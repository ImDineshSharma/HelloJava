// As we learned in previous file "HelloJava", this file has a class with the name IfApp
public class IfApp{
 public static void main(String[] args){ // this is our main method
  int x = 2; // we declare an integer type variable "x" and gave it the value "2"
  if (x < 4){ // here we put the condition that if the value of x is less than 4, keep printing "this is less than four"
   System.out.println("This is less than 4"); // this is the output we want when the condition is true
  }
 }
}
